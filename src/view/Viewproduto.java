    private void jbAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAlterarActionPerformed
        salvarAlterar = "alterar";
        this.volatilCampo(true);
        int linha = this.jtableProdutos.getSelectedRow();
        try {
            int codProduto = (int) this.jtableProdutos.getValueAt(linha, 0);
            /*
            recuperar dados do banco
            */
            modelProdutos = controllerProdutos.retornarProdutoController(codProduto);
            /*
            transformando float em string para nao dar o erro
            "java.lang.NumberFormatException: empty String"
            */
            this.jtfCodigo.setText(String.valueOf(modelProdutos.getCodProduto()));
            this.jtfNome.setText(modelProdutos.getNomeProd());
            this.jtfEstoque.setText(String.valueOf(modelProdutos.getQtEstoque()));
            this.jtfValor.setText(String.valueOf(modelProdutos.getValorProd()));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Nenhum código/registro selecionado", "Atenção!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jbAlterarActionPerformed

