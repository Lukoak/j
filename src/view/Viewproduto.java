    private void salvarProduto() {        // TODO add your handling code here:
        modelProdutos.setNomeProd(this.jtfNome.getText());
        modelProdutos.setQtEstoque(Float.parseFloat(this.jtfEstoque.getText()));
        modelProdutos.setValorProd(Float.parseFloat(this.jtfValor.getText()));
        if (controllerProdutos.salvarProdutoController(modelProdutos) > 0) {
            JOptionPane.showMessageDialog(this, "Cadastrado!", "ATENÇÃO!!!", JOptionPane.WARNING_MESSAGE);
            this.carregarProdutos();
            this.resetCampo();
            this.volatilCampo(false);
        } else {
            JOptionPane.showMessageDialog(this, "Inserção incorreta!", "ERRO", JOptionPane.ERROR_MESSAGE);
        }
    }

    ;
    
    private void alterarProduto() {
        modelProdutos.setNomeProd(this.jtfNome.getText());
        modelProdutos.setQtEstoque(Float.parseFloat(this.jtfEstoque.getText()));
        modelProdutos.setValorProd(Float.parseFloat(this.jtfValor.getText()));
        if (controllerProdutos.alterarProdutoController(modelProdutos)) {
            JOptionPane.showMessageDialog(this, "Alterado!", "ATENÇÃO!!!", JOptionPane.WARNING_MESSAGE);
            this.carregarProdutos();
            this.resetCampo();
            this.volatilCampo(false);
        } else {
            JOptionPane.showMessageDialog(this, "Alteração incorreta! Verifique os parâmetros ou permissões!",
                    "ERRO", JOptionPane.ERROR_MESSAGE);
        }
    }

    ;

    /*
    trava o campo
     */
    private void volatilCampo(boolean condicao) {
        jtfNome.setEnabled(condicao);
        jtfEstoque.setEnabled(condicao);
        jtfValor.setEnabled(condicao);
    }

    ;
    /*
    faz o campo ficar vazio
    */
    private void resetCampo() {
        jtfNome.setText("");
        jtfEstoque.setText("");
        jtfValor.setText("");
    }

    /*
    preencher tablea produtos c/ prod cadastrado banco, ou seja, atualiza
    a sheet
     */
    private void carregarProdutos() {
        listaModelProdutos = controllerProdutos.retornarListaProdutoController();
        DefaultTableModel modelo = (DefaultTableModel) jtableProdutos.getModel();
        modelo.setNumRows(0);
        int cont = listaModelProdutos.size();
        for (int i = 0; i < cont; i++) {
            modelo.addRow(new Object[]{
                listaModelProdutos.get(i).getCodProduto(),
                listaModelProdutos.get(i).getNomeProd(),
                listaModelProdutos.get(i).getQtEstoque(),
                listaModelProdutos.get(i).getValorProd(),});
        }
    }
